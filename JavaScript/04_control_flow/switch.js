//switch

const mon  = 2 ;

switch (mon) {
    case 1:
        console.log("Jan");
        break;
    case 2:
        console.log("Feb");
        break;
    case 3:
        console.log("Mar");
        break;
    case 4:
        console.log("Apr");
        break;
    default:
        console.log("Select Valid Mon")
        break;
}

const week  = "Monday" ;

switch (week) {
    case "Sunday":
        console.log("Jan");
        break;
    case "Monday":
        console.log("Feb");
        break;
    case "Tuesday":
        console.log("Mar");
        break;
    default:
        console.log("Select Valid Day")
        break;
}